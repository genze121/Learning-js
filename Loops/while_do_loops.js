// while loop

// Example - 1

let index = 1;
while (index <= 10) {
  console.log(`Value of index : ${index}`);
  index++;
}

// Example - 2

const array = [1, 2, 3, 4, 5];
let i = 0;
while (i < array.length) {
  console.log(`Array value : ${array[i]}`);
  i++;
}

